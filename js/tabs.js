function showsobrenos () {
    document.getElementById('tab-sobrenos').style.display = 'block'
    document.getElementById('tab-consultoria').style.display = 'none'
    document.getElementById('tab-arquiteto').style.display = 'none'
}

function showconsultoria () {
    document.getElementById('tab-sobrenos').style.display = 'none'
    document.getElementById('tab-consultoria').style.display = 'block'
    document.getElementById('tab-arquiteto').style.display = 'none'
}

function showarquiteto () {
    document.getElementById('tab-sobrenos').style.display = 'none'
    document.getElementById('tab-consultoria').style.display = 'none'
    document.getElementById('tab-arquiteto').style.display = 'block'
}